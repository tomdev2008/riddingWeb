public class Photo {
	/**
	 * id
	 */
	private long id;
	/**
	 * 原图路径
	 */
	private String originalPath="";
	/**
	 * 小图路径75
	 */
	private String smallPath="";
	/**
	 * 中图路径130
	 */
	private String midPath="";
	/**
	 * 大图路径260
	 */
	private String bigPath="";
	/**
	 * 方图路径75*75
	 */
	private String squarePath="";

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getOriginalPath() {
		return originalPath;
	}

	public void setOriginalPath(String originalPath) {
		this.originalPath = originalPath;
	}

	public String getSmallPath() {
		return smallPath;
	}

	public void setSmallPath(String smallPath) {
		this.smallPath = smallPath;
	}

	public String getMidPath() {
		return midPath;
	}

	public void setMidPath(String midPath) {
		this.midPath = midPath;
	}

	public String getBigPath() {
		return bigPath;
	}

	public void setBigPath(String bigPath) {
		this.bigPath = bigPath;
	}

	public String getSquarePath() {
		return squarePath;
	}

	public void setSquarePath(String squarePath) {
		this.squarePath = squarePath;
	}

}
