package com.ridding.meta;

import java.io.Serializable;

/**
 * @author zhengyisheng E-mail:zhengyisheng@corp.netease.com
 * @version CreateTime 2012-3-5 11:47:31 Class Description 外域资源账户信息
 */
public class SourceAccount implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -5485201558906668446L;
	/**
	 * 表id
	 */
	private long id;
	/**
	 * 外域资源登录用的用户id
	 */
	private long accessUserId;

	/**
	 * 用户id
	 */
	private long userId;
	/**
	 * 登录用的accesstoken
	 */
	private String accessToken = "";
	/**
	 * 资源类型
	 */
	private int sourceType;
	/**
	 * 创建时间
	 */
	private long createTime;
	/**
	 * 用户名,不存数据库
	 */
	private String accessUserName;

	public String getAccessUserName() {
		return accessUserName;
	}

	public void setAccessUserName(String accessUserName) {
		this.accessUserName = accessUserName;
	}

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

	public long getAccessUserId() {
		return accessUserId;
	}

	public void setAccessUserId(long accessUserId) {
		this.accessUserId = accessUserId;
	}

	public String getAccessToken() {
		return accessToken;
	}

	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}

	public int getSourceType() {
		return sourceType;
	}

	public void setSourceType(int sourceType) {
		this.sourceType = sourceType;
	}

	public long getCreateTime() {
		return createTime;
	}

	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

}
