public class Compress {
	/**
	 * 构造函数
	 * 
	 * @param width
	 * @param height
	 * @param quartility
	 */
	public Compress(int width, int height, int quartility) {
		this.width = width;
		this.height = height;
		this.quartility = quartility;
	}

	/**
	 * 宽
	 */
	private int width;
	/**
	 * 高
	 */
	private int height;
	/**
	 * 质量
	 */
	private int quartility;

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getQuartility() {
		return quartility;
	}

	public void setQuartility(int quartility) {
		this.quartility = quartility;
	}

}
